    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        try {
            int linha = tableListagem.getSelectedRow();
            String codAtleta;
            FrameBateria frameBateria = new FrameBateria();
            codAtleta = (String) tableListagem.getValueAt(linha, 0);
            String numBateria = funcoes.pegaUltimoCaracter(this);
            int codCompeticao = pesquisaCompeticao.pesquisaCompeticao(this.lbNomeCompeticao.getText());
            int codModalidade = pesquisaModalidade.pesquisaModalidade(this.getName());
            String bateria = numBateria;
            deletaCompetidor.deletarCompetidor(codAtleta, Integer.parseInt(numBateria));

            model = funcoes.montaTabelaCompetidor(frameBateria.tableListagem, frameBateria.spCompetidores);
            pesquisaCompetidor.pesquisaCompetidor(model, bateria, codModalidade, codCompeticao);
            frameBateria.setName(this.getName());
            frameBateria.setTitle("MODALIDADE " + this.getName() + " - BATERIA " + bateria);
            frameBateria.lbNomeCompeticao.setText(this.lbNomeCompeticao.getText());
            frameBateria.setVisible(true);
            this.dispose();
        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Selecine um atleta.");
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtletas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FrameBateria.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btRemoverActionPerformed

