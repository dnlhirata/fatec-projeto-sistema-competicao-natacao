    private void btSelecionarModalidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSelecionarModalidadeActionPerformed
        int codModalidade, numBaterias, codCompeticao;
        if (this.cbModalidade.getSelectedItem().equals("SELECIONE A MODALIDADE") || this.cbModalidade.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(this, "Selecione a modalidade");
        } else {
            try {
                String nomeModalidade = (String) this.cbModalidade.getSelectedItem();
                FrameSelecaoBateria frameBateria = new FrameSelecaoBateria();
                frameBateria.lbNomeCompeticao.setText((String) this.lbNomeCompeticao.getText());
                frameBateria.setName(nomeModalidade);
                codModalidade = pesquisaModalidade.pesquisaModalidade(nomeModalidade);
                codCompeticao = pesquisaCompeticao.pesquisaCompeticao(this.lbNomeCompeticao.getText());
                numBaterias = pesquisaBateria.qtdBaterias(codModalidade, codCompeticao);
                for (int i = 1; i <= numBaterias; i++) {
                    if (i == numBaterias) {
                        frameBateria.cbBaterias.addItem("Final");
                    } else {
                        frameBateria.cbBaterias.addItem(Integer.toString(i));
                    }
                }
                frameBateria.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(FrameSelecaoModalidade.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(FrameSelecaoModalidade.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }

    }//GEN-LAST:event_btSelecionarModalidadeActionPerformed

