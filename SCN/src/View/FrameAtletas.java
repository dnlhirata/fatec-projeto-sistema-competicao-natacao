    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        try {
            FrameAdicionaAtleta frameAtleta = new FrameAdicionaAtleta();
            frameAtleta.lbNomeCompeticao.setText(this.lbNomeCompeticao.getText());
            int codCompeticao = pesquisaCompeticao.pesquisaCompeticao(this.lbNomeCompeticao.getText());
            ArrayList<String> equipes = pesquisaEquipe.carregaComboBoxEquipes(codCompeticao);
            for (int i = 0; i < equipes.size(); i++) {
                frameAtleta.cbEquipe.addItem(equipes.get(i));
            }
            frameAtleta.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtletas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrameAtletas.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed

        try {
            int codAtleta = pesquisaAtleta.validaLinhaSelecionada(tableListagem);
            ResultSet rs = pesquisaAtleta.pesquisaAtleta(codAtleta);

            FrameAlteraAtleta frameAtleta = new FrameAlteraAtleta();
            frameAtleta.lbNomeCompeticao.setText(this.lbNomeCompeticao.getText());

            while (rs.next()) {
                frameAtleta.ftfCpf.setText(rs.getString("Cpf"));
                frameAtleta.ftfCpf.setEnabled(false);
                frameAtleta.tfNome.setText(rs.getString("Nome"));
                frameAtleta.tfEndereco.setText(rs.getString("Endereco"));
                frameAtleta.ftfDtNascimento.setText(rs.getString("Dtnascimento"));
                frameAtleta.cbEstado.setSelectedItem(rs.getString("Estado"));
                frameAtleta.cbCidade.setSelectedItem(rs.getString("Cidade"));
                frameAtleta.cbStatus.setSelectedItem(rs.getString("Status"));
                frameAtleta.rbMasculino.setSelected(rs.getString("Sexo").equals("Masculino"));
                frameAtleta.rbFeminino.setSelected(rs.getString("Sexo").equals("Feminino"));
                frameAtleta.cbEquipe.setSelectedItem(rs.getString("Nomeequipe"));
            }

            int codCompeticao = pesquisaCompeticao.pesquisaCompeticao(this.lbNomeCompeticao.getText());
            ArrayList<String> equipes = pesquisaEquipe.carregaComboBoxEquipes(codCompeticao);
            for (int i = 0; i < equipes.size(); i++) {
                frameAtleta.cbEquipe.addItem(equipes.get(i));
            }
            
            frameAtleta.setVisible(true);
            this.dispose();

        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Selecine um atleta.");
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtletas.class.getName()).log(Level.SEVERE, null, ex);
        }

        
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        try {
            int codAtleta = pesquisaAtleta.validaLinhaSelecionada(tableListagem);
            deletaAtleta.deletarAtleta(codAtleta);
            int codCompeticao = pesquisaCompeticao.pesquisaCompeticao(this.lbNomeCompeticao.getText());
            remove(this.tableListagem);
            DefaultTableModel model = constroiTabelaAtleta.estruturaTabelaAtleta(this.tableListagem, this.spAtletas);
            pesquisaAtleta.carregaJTableAtletas(model, codCompeticao);
        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Selecine um atleta.");
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtletas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FrameAtletas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btRemoverActionPerformed

